package br.com.bandtec.projeto01.nivelamento1;

public class Programa1 {

    // método main: é o que transaforma a classe num programa (ou seja, torna a class executável)
    // main<TAB> ou psvm<TAB> (netbeans 11)
    public static void main(String[] args) {
        // sout <TAB>
        // Exibindo frase na saída padrão
        System.out.println("Meu programa em Java");
        
        // contatenação
        System.out.println("Boa tarde "+"meus queridos");
       
        
        // Variáveis em Java
        
        // Variáveis de texto (ou alfanuméricas)
        String bairro = "Saúde";
        // em java, texto é String (com S maiúsculo SEMPRE)
        String cidade = "São Paulo";
        
        // concatenando texto e variável
        System.out.println("Você mora no bairro "+bairro+" na cidade de "+cidade);
        
        // em Java os textos são sempre entre ASPAS DUPLAS!!! Aspas simples NÃO ROLA!
        
        
        
    }
    
}


