    private void btDepositarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDepositarActionPerformed
        Double valorDeposito = Double.valueOf(tfValor.getText());
        
        Operacao novoDeposito = new Operacao(valorDeposito);
        operacoes.add(novoDeposito);
        
        Long depositos = operacoes.stream()
                .filter(operacao -> operacao.isDeposito())
                .count();
        
//        Long saques = operacoes.stream()
//                .filter(operacao -> operacao.isSaque())
//                .count();

        Long saques = operacoes.size() - depositos;
        
        lbDepositos.setText(depositos.toString());
        lbSaques.setText(saques.toString());
        
        Double totalDepositado = 0.0;
        for (Operacao operacao : operacoes) {
            totalDepositado += operacao.getValor();
        }
        lbTotal.setText(totalDepositado.toString());
        
    }//GEN-LAST:event_btDepositarActionPerformed

    private void btSacarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSacarActionPerformed
        Double valorSaque = Double.valueOf(tfValor.getText()) * -1;
        
        Operacao novoSaque = new Operacao(valorSaque);
        operacoes.add(novoSaque);
        
        Long depositos = operacoes.stream()
                .filter(operacao -> operacao.isDeposito())
                .count();

        Long saques = operacoes.size() - depositos;
        
        lbDepositos.setText(depositos.toString());
        lbSaques.setText(saques.toString());
        
        Double totalDepositado = 0.0;
        for (Operacao operacao : operacoes) {
            totalDepositado += operacao.getValor();
        }
        lbTotal.setText(totalDepositado.toString());
    }//GEN-LAST:event_btSacarActionPerformed

